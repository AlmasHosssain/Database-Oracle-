CREATE OR REPLACE FUNCTION KOP(AREA NUMBER) 
RETURN NUMBER IS
PI CONSTANT NUMBER(7,3):=3.1416;
TOT NUMBER(7,3);

BEGIN 
  TOT:=PI * AREA * AREA;
  RETURN TOT;
END;
/
SET SERVEROUTPUT ON;
BEGIN
  DBMS_OUTPUT.PUT_LINE(KOP(25));
END;
/
DECLARE 
 V_KOP NUMBER;
BEGIN
  V_KOP:= KOP(30);
  DBMS_OUTPUT.PUT_LINE(V_KOP);
END;
/