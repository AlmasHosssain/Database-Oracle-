SET SERVEROUTPUT ON;
DECLARE
  V_EMP  DATA_BASE%ROWTYPE;
BEGIN
  SELECT P_NAME,P_CITY INTO V_EMP.P_NAME,V_EMP.P_CITY FROM DATA_BASE
  WHERE P_ID=30;
  
  DBMS_OUTPUT.PUT_LINE(V_EMP.P_NAME|| ' '||V_EMP.P_CITY );
 -- DBMS_OUTPUT.PUT_LINE(V_EMP.P_CITY);
END;
/
SET SERVEROUTPUT ON;
DECLARE
  CURSOR KOOP IS SELECT P_NAME,P_CITY FROM DATA_BASE 
  WHERE P_ID<=30;
  
  V_KMP  KOOP%ROWTYPE;

BEGIN
  OPEN KOOP;
  LOOP
    FETCH KOOP INTO V_KMP;
    EXIT WHEN KOOP%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE(V_KMP.P_NAME|| ' '||V_KMP.P_CITY );
  END LOOP;
END;
/
