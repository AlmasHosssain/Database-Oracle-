---------TABLE BASED RECORDTYPE---------

SET SERVEROUTPUT ON;
DECLARE
  V_RECORD STUDET%ROWTYPE;
BEGIN
   SELECT * INTO V_RECORD FROM STUDET
  WHERE CUS_ID = 1;
  
  DBMS_OUTPUT.PUT_LINE(V_RECORD.F_NAME ||' '|| V_RECORD.L_NAME);
  DBMS_OUTPUT.PUT_LINE(V_RECORD.SALARY);
END;
/

DECLARE 
V_ROS STUDET%ROWTYPE;
BEGIN 
  SELECT F_NAME,CITY INTO V_ROS.F_NAME,V_ROS.CITY
  FROM STUDET WHERE CUS_ID = 1;
  DBMS_OUTPUT.PUT_LINE(V_ROS.F_NAME||' '||V_ROS.CITY);
END;
/


