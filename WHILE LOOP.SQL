SET SERVEROUTPUT ON;
DECLARE
  V_COUNTER  NUMBER:=1;
  V_RESULT NUMBER;
BEGIN
  WHILE V_COUNTER <= 10 LOOP
  V_RESULT := 19 * V_COUNTER;
  DBMS_OUTPUT.PUT_LINE('19'||' X'|| V_COUNTER||' ='||V_RESULT);
  V_COUNTER := V_COUNTER+1;
  END LOOP;
END;
/
DECLARE
 V_TEST BOOLEAN := TRUE;
 V_NUM NUMBER:=0;
BEGIN
  WHILE V_TEST LOOP
    V_NUM:=V_NUM+1;
    dbms_output.put_line(V_NUM);
    IF V_NUM = 10 THEN
    V_TEST := FALSE;
    END IF;
  END LOOP;

END;