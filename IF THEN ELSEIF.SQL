SET SERVEROUTPUT ON;
DECLARE
  V_PLACE VARCHAR2(30):= '&ENTER_THE_NAME_OF_PALCE';
BEGIN
  IF V_PLACE = 'DHAKA'
  THEN
  DBMS_OUTPUT.PUT_LINE(V_PLACE||' IS THE CAPITAL OF BANGLADESH');
  ELSIF V_PLACE='CUMILLA' THEN
  DBMS_OUTPUT.PUT_LINE(V_PLACE||' IS THE TOWN OF SWEET');
  ELSIF V_PLACE='CTG' THEN
  DBMS_OUTPUT.PUT_LINE(V_PLACE||' IS THE PORT CITY');
  ELSE
  DBMS_OUTPUT.PUT_LINE('ENTER AGAIN');
  END IF;
  DBMS_OUTPUT.PUT_LINE('THANK YOU FOR CONNECTING');
END;