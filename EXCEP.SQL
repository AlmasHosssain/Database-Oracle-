SET SERVEROUTPUT ON;
DECLARE 
  V_NUMBER   NUMBER:=24;
  V_DEVIDER  NUMBER:=&GIVE_YOUR_INPUT;
  V_RESULT   NUMBER;
  V_EXCEP   EXCEPTION;
  
BEGIN
  IF V_DEVIDER= 0 THEN
    RAISE V_EXCEP;
  END IF;
  V_RESULT := V_NUMBER/V_DEVIDER;
  DBMS_OUTPUT.PUT_LINE('RESULT IS '||V_RESULT);
  
  EXCEPTION WHEN V_EXCEP THEN
  DBMS_OUTPUT.PUT_LINE('BOSS ERROR HAS BEEN DONE');
END;
/