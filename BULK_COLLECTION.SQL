SET SERVEROUTPUT ON;
DECLARE 
  CURSOR DFG IS 
  SELECT F_NAME FROM STUDENT;
  
  TYPE NASTADE_TABLE IS TABLE OF VARCHAR2(30);
  V_VAR NASTADE_TABLE;

BEGIN 
  OPEN DFG;
   LOOP
      FETCH DFG BULK COLLECT INTO V_VAR;
      FOR ID_X IN V_VAR.FIRST..V_VAR.LAST
        LOOP
          DBMS_OUTPUT.PUT_LINE(ID_X||' '||V_VAR(ID_X));
        END LOOP;
      EXIT WHEN DFG%NOTFOUND;
   END LOOP;
  CLOSE DFG;
END;
/
DECLARE
  TYPE DF IS TABLE OF VARCHAR2(30);
  V_VAR DF;
BEGIN
  SELECT L_NAME BULK COLLECT INTO V_VAR FROM STUDENT;
  FOR IG IN 1..V_VAR.COUNT
   LOOP
    DBMS_OUTPUT.PUT_LINE(V_VAR(IG));
   END LOOP;
END;
/
  
