CREATE TABLE TRE(
  A_NAME VARCHAR2(30)
);
ALTER TABLE TRE ADD A_CIT VARCHAR2(30); 
ALTER TABLE TRE DROP COLUMN A_CIT;
DESC TRE;
CREATE TABLE QER(
  A_SUBJECT VARCHAR2(30)
);

CREATE VIEW A_VIEW AS SELECT A_NAME,A_SUBJECT FROM TRE,QER;


CREATE OR REPLACE TRIGGER INS_IN
INSTEAD OF INSERT ON A_VIEW
FOR EACH ROW
ENABLE
DECLARE
BEGIN
  INSERT INTO TRE (A_NAME) VALUES (:NEW.A_NAME);
  INSERT INTO QER (A_SUBJECT) VALUES (:NEW.A_SUBJECT);
END;
/

INSERT INTO A_VIEW (A_NAME,A_SUBJECT) VALUES('ALMAS','ICT');
INSERT INTO A_VIEW (A_NAME,A_SUBJECT) VALUES('SHOJOL','PA');
SELECT * FROM A_VIEW;

CREATE OR REPLACE TRIGGER INS_DELETE
INSTEAD OF INSERT ON A_VIEW
FOR EACH ROW
ENABLE
DECLARE
BEGIN
  DELETE FROM TRE WHERE A_NAME =:OLD.A_NAME;
  DELETE FROM QER WHERE A_SUBJECT =:OLD.A_SUBJECT;
END;
/
DELETE FROM A_VIEW WHERE A_NAME ='SHOJOL';
























