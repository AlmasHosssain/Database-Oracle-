CREATE OR REPLACE TRIGGER TRT
BEFORE INSERT OR DELETE OR UPDATE ON MAN2
FOR EACH ROW
ENABLE
BEGIN
  IF INSERTING THEN
    INSERT INTO MAN_BACKUP2 (M_NAME) VALUES(:NEW.M_NAME);
  ELSIF DELETING THEN
    DELETE FROM MAN_BACKUP2 WHERE M_NAME=:OLD.M_NAME
  ELSIF UPDATING THEN 
    UPDATE MAN_BACKUP2 SET M_NAME= :NEW.M_NAME WHERE M_NAME = :OLD.M_NAME;
  END IF;
END;
