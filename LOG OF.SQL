CREATE TABLE LOG_OFF(
  EVENT_TYPE VARCHAR2(20),
  LOGON_DATE DATE,
  LOGON_TIME VARCHAR2(20),
  LOGOF_DATE DATE,
  LOGOF_TIME VARCHAR2(20)
);

CREATE OR REPLACE TRIGGER LOGOF_AUDIT
AFTER LOGON ON SCHEMA
BEGIN
  INSERT INTO LOG_OFF VALUES(
    ORA_SYSEVENT,
    NULL,
    NULL,
    SYSDATE,
    TO_CHAR(SYSDATE,'HH24:MI:SI')
  );
  COMMIT;
END;
/

SELECT * FROM LOG_OFF;
