SET SERVEROUTPUT ON;
DECLARE
COUNTER NUMBER(20):=0;
RESULT NUMBER(30);
BEGIN
  LOOP
    COUNTER := COUNTER+1;
    RESULT := COUNTER*19;
    DBMS_OUTPUT.PUT_LINE('19' || ' X'||  COUNTER || ' ='   ||   RESULT);
    IF COUNTER=10
      THEN 
       EXIT;
    END IF;
  END LOOP;
END;
/
DECLARE
  COT NUMBER:=0;
  RES NUMBER;
BEGIN
  LOOP
    COT := COT+1;
    RES := COT * 19;
    DBMS_OUTPUT.PUT_LINE('19'||' X'|| COT|| ' ='|| RES);
    EXIT WHEN COT>=10;
  END LOOP;
END;