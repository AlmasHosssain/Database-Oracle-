DECLARE
V_NAME VARCHAR2(20 BYTE);
CURSOR BOSS IS
SELECT F_NAME FROM STUDENT
WHERE CUS_ID<7;

BEGIN
  OPEN BOSS;
   LOOP 
    FETCH BOSS INTO V_NAME;
    DBMS_OUTPUT.PUT_LINE(V_NAME);
    EXIT WHEN BOSS%NOTFOUND;
   END LOOP;
  CLOSE BOSS;
END;
/
SET SERVEROUTPUT ON;

DECLARE
V_LNAME VARCHAR2(20 BYTE);
CURSOR KOP(JK NUMBER) IS
SELECT F_NAME ||' '|| L_NAME AS NAME FROM STUDENT
WHERE CUS_ID<JK;

BEGIN
  OPEN KOP(3);
   LOOP
    FETCH KOP INTO V_LNAME;
    DBMS_OUTPUT.PUT_LINE(V_LNAME);
    EXIT WHEN KOP%NOTFOUND;
   END LOOP;
  CLOSE KOP;
END;
/
SET SERVEROUTPUT ON;

DECLARE
  V_HNAME VARCHAR2(40 BYTE);
  V_SALARY NUMBER;
  -----CURSOR START------
  CURSOR ALMAS(ZIM NUMBER :=3) IS
  SELECT F_NAME||' '||L_NAME,SALARY FROM STUDENT
  WHERE CUS_ID< ZIM;

BEGIN
  OPEN ALMAS;
   LOOP
    FETCH ALMAS INTO V_HNAME,V_SALARY;
    DBMS_OUTPUT.PUT_LINE(V_HNAME||' '||V_SALARY);
    EXIT WHEN ALMAS%NOTFOUND;
   END LOOP;
  CLOSE ALMAS;
END;
/
SET SERVEROUTPUT ON;

DECLARE
  CURSOR FTM IS 
  SELECT F_NAME||' '||L_NAME,CITY,SALARY
  FROM STUDENT 
  WHERE CUS_ID<4;
  
BEGIN
  FOR L_INDEX IN FTM
    LOOP 
      DBMS_OUTPUT.PUT_LINE(L_INDEX.F_NAME||' '||L_INDEX.L_NAME);
    END LOOP;
END;
