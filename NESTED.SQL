SET SERVEROUTPUT ON;
DECLARE
  TYPE NEST IS TABLE OF NUMBER;
  V_NET NEST := NEST(8,4,2,34,5,23490,84,234);
BEGIN
  DBMS_OUTPUT.PUT_LINE('FIRST NUMBER IS '|| V_NET(1));
  DBMS_OUTPUT.PUT_LINE('SIXET NUMBER IS '|| V_NET(6));
  DBMS_OUTPUT.PUT_LINE('FORTH NUMBER IS '|| V_NET(4));
END;
/
DECLARE 
  TYPE KOOP IS TABLE OF NUMBER;
  V_XOX KOOP := KOOP(8,4,2,34,5,23490,84,234);
BEGIN
  FOR K IN REVERSE 1..V_XOX.COUNT
  LOOP
     DBMS_OUTPUT.PUT_LINE('THE VARIABLE OF KOOP IS ' || V_XOX(K));
  END LOOP;
END;
/