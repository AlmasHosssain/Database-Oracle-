CREATE TABLE MAN(
  M_NAME VARCHAR2(30)
);
DESC MAN;

CREATE TABLE MAN_BACKUP AS SELECT * FROM MAN;
DESC man_backup;
SET SERVEROUTPUT ON;
CREATE OR REPLACE TRIGGER MAIN_BACKUP
BEFORE INSERT OR DELETE OR UPDATE ON MAN_BACKUP
FOR EACH ROW
ENABLE
BEGIN
  IF INSERTING THEN
    INSERT INTO MAN_BACKUP (M_NAME) VALUES(:NEW.M_NAME);
  ELSIF DELETING THEN
    DELETE FROM MAN_BACKUP WHERE M_NAME=:OLD.M_NAME
  ELSIF UPDATING THEN 
    UPDATE MAN_BACKUP SET M_NAME= :NEW.M_NAME WHERE M_NAME = :OLD.M_NAME;
  END IF;
END;
