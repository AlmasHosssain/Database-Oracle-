CREATE OR REPLACE FUNCTION F_NAME(V_1 NUMBER,
V_2 NUMBER DEFAULT 0 ,V_3 NUMBER)
 RETURN NUMBER
 IS
 BEGIN
 DBMS_OUTPUT.PUT_LINE('V_1->'||' '|| V_1);
 DBMS_OUTPUT.PUT_LINE('V_2->'||' '|| V_2);
 DBMS_OUTPUT.PUT_LINE('V_3->'||' '|| V_3);
  V_RESULT:= V_1+V_2+V_3;
 RETURN V_RESULT;
END;
/
DECLARE
 V_RES NUMBER;
BEGIN
  V_RES := F_NAME(V_1=>5,V_3=>3);
  DBMS_OUTPUT.PUT_LINE('V_RES->'||' '|| V_RES);
END;
/