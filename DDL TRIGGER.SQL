CREATE TABLE DDL_AUDIT(
  ORERATION_TIME DATE,
  USER_NAME VARCHAR2(30),
  OBJECT_TYPE VARCHAR2(30),
  OBJECT_NAME VARCHAR2(30),
  OPERATON_NAME VARCHAR2(30)
);

CREATE OR REPLACE TRIGGER SYSTEM_AUDIT
AFTER DDL ON SCHEMA
BEGIN
  INSERT INTO DDL_AUDIT VALUES(
    SYSDATE,
    SYS_CONTEXT ('USERENV','CURRENT_USER'),
    ORA_DICT_OBJ_TYPE,
    ORA_DICT_OBJ_NAME,
    ORA_SYSEVENT
  );
END;
/
