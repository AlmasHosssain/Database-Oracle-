DECLARE
  CURSOR CUR_RECORD IS 
  SELECT F_NAME,SALARY FROM STU WHERE CUS_ID<4;
  V_REC CUR_RECORD%ROWTYPE;
BEGIN
  FOR V_REC IN CUR_RECORD
    LOOP
      DBMS_OUTPUT.PUT_LINE(V_REC.F_NAME||' '||V_REC.SALARY);
    END LOOP;
END;
/
DECLARE
  CURSOR CUR_RECRD IS 
  SELECT F_NAME,SALARY FROM STU WHERE CUS_ID<3;
  
  V_RC CUR_RECRD%ROWTYPE;
BEGIN
  OPEN CUR_RECRD;
    LOOP
     FETCH CUR_RECRD INTO V_RC;
     EXIT WHEN CUR_RECRD%NOTFOUND;
     DBMS_OUTPUT.PUT_LINE(V_RC.F_NAME||' '||V_RC.SALARY);
    END LOOP;
  CLOSE CUR_RECRD;
END;
/
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  