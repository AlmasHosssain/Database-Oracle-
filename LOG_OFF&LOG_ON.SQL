CREATE TABLE LOG_OF(
  USER_NAME       VARCHAR2(30),
  OPERATION_TYPE  VARCHAR2(30),
  LOG_OFF_DATE    VARCHAR2(30),
  LOG_OFF_TIME    VARCHAR2(30),
  LOG_ON_DATE     VARCHAR2(30),
  LOG_ON_TIME     VARCHAR2(30)
);

CREATE OR REPLACE TRIGGER LOOG
BEFORE LOGOFF OR LOGON ON DATABASE
ENABLE
DECLARE
BEGIN
  INSERT INTO LOG_OF VALUES(
    USER,
    ORA_SYSEVENT,
    SYSDATE,
    TO_CHAR(SYSDATE, 'HH24:MI:SS'),
    SYSDATE,
    TO_CHAR(SYSDATE, 'HH24:MI:SS'),
  );
END;
/
SELECT * FROM LOG_OF; 