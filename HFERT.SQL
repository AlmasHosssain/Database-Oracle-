DESC CUSTOMER;
SET SERVEROUTPUT ON;
CREATE  OR REPLACE TRIGGER UP_TO
BEFORE INSERT OR DELETE OR UPDATE ON CUSTOMER
FOR EACH ROW
ENABLE 
DECLARE 
  V_USER VARCHAR2(100);
BEGIN
  SELECT USER INTO V_USER FROM DUAL;
  IF INSERTING THEN
    DBMS_OUTPUT.PUT_LINE('INSERTION IS DONE BY ' || V_USER);
  ELSIF DELETING THEN
    DBMS_OUTPUT.PUT_LINE('DELETION IS DONE BY '|| V_USER);
  ELSIF UPDATING  THEN
   DBMS_OUTPUT.PUT_LINE('UPDATE IS DONE BY '|| V_USER);
  END IF;
END;
/
