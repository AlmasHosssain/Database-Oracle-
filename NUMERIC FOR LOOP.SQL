SET SERVEROUTPUT ON;
BEGIN
  FOR V_COUNTER IN 1..10 LOOP
    DBMS_OUTPUT.PUT_LINE(V_COUNTER);
  END LOOP;
END;
/
BEGIN
  FOR V_COUNTER IN REVERSE 1..10 LOOP
    DBMS_OUTPUT.PUT_LINE(V_COUNTER);
  END LOOP;
END;
/

DECLARE
  V_RESULT NUMBER;
BEGIN
  FOR V_COUNT IN 1..10 LOOP
    V_RESULT:= 19 * V_COUNT;
    DBMS_OUTPUT.PUT_LINE('19'||' X'||' '|| V_COUNT||' ='   ||V_RESULT);
  END LOOP;
END;