CREATE TABLE LOG_ON(
  EVENT_TYPE VARCHAR2(20),
  LOGON_DATE DATE,
  LOGON_TIME VARCHAR2(20),
  LOGOF_DATE DATE,
  LOGOF_TIME VARCHAR2(20)
);

CREATE OR REPLACE TRIGGER LOGON_AUDIT
AFTER LOGON ON SCHEMA
BEGIN
  INSERT INTO LOG_ON VALUES(
    ORA_SYSEVENT,
    SYSDATE,
    TO_CHAR(SYSDATE,'HH24:MI:SI'),
    NULL,
    NULL
  );
  COMMIT;
END;
/
DISCONNECT SYSTEM;
CONNECT SYSTEM/SYSTEM;
SELECT * FROM LOG_ON;
