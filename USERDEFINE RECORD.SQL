  EMPLOE_ID NUMBER,
  DEPARTMENT_NAME VARCHAR2(20),
  CONSTRAINT FK FOREIGN KEY (EMPLOE_ID)
  REFERENCES EMPLOOYE(EMPLOE_ID)  
);

