DECLARE
  CREATE OR REPLACE FUNCTION KOP(RADIUS NUMBER) RETURN NUMBER IS
  PI CONSTANT NUMBER(7,3) := 3.1416;
  V_RESULT NUMBER(7,3);
  
BEGIN
  V_RESULT:= PI*RADIUS*RADIUS;
  RETURN V_RESULT;
END;
