SET SERVEROUTPUT ON;
DECLARE
  V_NAME VARCHAR2(30);
  CURSOR BIG IS SELECT P_NAME FROM PHP WHERE P_ID<30;
BEGIN
  OPEN BIG;
  LOOP
    FETCH BIG INTO V_NAME;
    EXIT WHEN BIG%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE(V_NAME);
  END LOOP;
  CLOSE BIG;
END;
/

DECLARE 
  VI_NAME VARCHAR2(30);
  VI_CITY VARCHAR2(30);
  
  CURSOR BIG2(NUB NUMBER) IS SELECT P_NAME,P_CITY FROM PHP
  WHERE P_ID<NUB;
BEGIN
  OPEN BIG2(30);
  LOOP
    FETCH BIG2 INTO VI_NAME,VI_CITY;
    EXIT WHEN BIG2%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE(VI_NAME||' '||'COMEES FROM '||VI_CITY);
  END LOOP;
END;
/

DECLARE 
  VI_NAME VARCHAR2(30);
  VI_CITY VARCHAR2(30);
  
  CURSOR BIG3(NUB NUMBER :=30) IS SELECT P_NAME,P_CITY FROM PHP
  WHERE P_ID<NUB;
BEGIN
  OPEN BIG3(20);
  LOOP
    FETCH BIG3 INTO VI_NAME,VI_CITY;
    EXIT WHEN BIG3%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE(VI_NAME||' '||'COMEES FROM '||VI_CITY);
  END LOOP;
END;
/

DECLARE 
  CURSOR BIG_FOR (KUK NUMBER) IS SELECT P_NAME,P_CITY
  FROM PHP WHERE P_ID<=KUK;
BEGIN
  FOR L_INX IN BIG_FOR(20)
  LOOP
    DBMS_OUTPUT.PUT_LINE
    (L_INX.P_NAME||' COMEEES FROM '||L_INX.P_CITY);
  END LOOP;
END;
